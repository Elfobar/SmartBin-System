  float calculatedDistance = 0.0;
  if (distance > 50){
    calculatedDistance = 0.0;
  }else{
    calculatedDistance = 100.0 - ((distance/50.0) * 100.0);
  }

  _tft.fillRoundRect(_x2 + 20, _y2 + 120, _w2 - 20, _h2 + 20, 5, TFT_BLACK);
